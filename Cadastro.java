    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  try{
 Class.forName("com.mysql.cj.jdbc.Driver"); 
 Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/alunos","root","");
 String UserId = jTextField1.getText();
 String Nome = jTextField2.getText();
 String Senha = jTextField3.getText();
 String RA =   jTextField4.getText();
 String Curso = jTextField5.getText();
 String Unidade = jTextField6.getText();
 
 
 
 String sql = "INSERT INTO tabela_alunos (UserId, Nome, Senha, RA,Curso,Unidade)  VALUES (?, ?, ?, ?, ?, ?)";
 PreparedStatement stmt = con.prepareStatement(sql);
stmt.setString(1, UserId);
        stmt.setString(2, Nome);
        stmt.setString(3, Senha);
        stmt.setString(4, RA);
        stmt.setString(5, Curso);
        stmt.setString(6, Unidade);
        int linhasAfetadas = stmt.executeUpdate();
        
        if (linhasAfetadas > 0) {
            showMessageDialog(this, "Registro incluído com sucesso!");
        } else {
            showMessageDialog(this, "Falha ao incluir o registro.");
        }
 

 jTextField1.setText("");
 jTextField2.setText("");
 jTextField3.setText("");
 jTextField4.setText("");
 jTextField5.setText("");
 jTextField6.setText("");
jTextField1.requestFocus();
 con.close();
 stmt.close();
 } catch (ClassNotFoundException e) {
        showMessageDialog(this, "Driver JDBC não encontrado: " + e.getMessage());
 }
  catch( SQLException e){
     showMessageDialog(this, "Comando SQL com problemas " + e.getMessage() );
 }
                                            


    }//GEN-LAST:event_jButton1ActionPerformed

